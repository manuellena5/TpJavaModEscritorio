package entidades;

public class Tipo_Clases {

	private int id_tipoclase;
	private String nombre;
	private int cantMaxClasesAAsistir;
	
	public int getId_tipoclase() {
		return id_tipoclase;
	}
	public void setId_tipoclase(int id_tipoclase) {
		this.id_tipoclase = id_tipoclase;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getCantMaxClasesAAsistir() {
		return cantMaxClasesAAsistir;
	}
	public void setCantMaxClasesAAsistir(int cantMaxClasesAAsistir) {
		this.cantMaxClasesAAsistir = cantMaxClasesAAsistir;
	}
	
	
}
