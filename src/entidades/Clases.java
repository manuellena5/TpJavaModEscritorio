package entidades;

public class Clases {
	
	private int id_clase;
	private String nombre;
	private int cupo;
	private int id_tipoclase;
	
	public int getId_clase() {
		return id_clase;
	}
	public void setId_clase(int id_clase) {
		this.id_clase = id_clase;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getCupo() {
		return cupo;
	}
	public void setCupo(int cupo) {
		this.cupo = cupo;
	}
	public int getId_tipoclase() {
		return id_tipoclase;
	}
	public void setId_tipoclase(int id_tipoclase) {
		this.id_tipoclase = id_tipoclase;
	}

}
